  uint8_t commandChecksum(const uint8_t *frame)
  {
    uint16_t sum = 0;
    for (size_t index = 2; index <= 6; ++index)
    {
      sum += frame[index];
    }
    return static_cast<uint8_t>(sum & 0xFF);
  }

  void appendRawUartByte(const uint8_t value)
  {
    char hexByte[3];
    snprintf_P(hexByte, sizeof(hexByte), PSTR("%02X"), value);
    rawUartTail += hexByte;
    if (rawUartTail.length() > kRawUartTailHexChars)
    {
      rawUartTail.remove(0, rawUartTail.length() - kRawUartTailHexChars);
    }
    rawUartByteCount++;
    lastRawUartAtMs = millis();
  }

  String formatRemoteFrameHex(const uint8_t *frame)
  {
    char frameHex[24];
    snprintf_P(
        frameHex,
        sizeof(frameHex),
        PSTR("%02X%02X%02X%02X%02X%02X%02X%02X"),
        frame[0],
        frame[1],
        frame[2],
        frame[3],
        frame[4],
        frame[5],
        frame[6],
        frame[7]);
    return String(frameHex);
  }

  bool parseRemoteFrame(const uint8_t *frame)
  {
    const uint16_t header = static_cast<uint16_t>(frame[0] << 8) | frame[1];
    if (header != kRemoteHeader)
    {
      return false;
    }

    if (commandChecksum(frame) != frame[7])
    {
      return false;
    }

    const uint16_t commandHigh = static_cast<uint16_t>(frame[2] << 8) | frame[3];
    const uint32_t commandLow = (static_cast<uint32_t>(frame[4]) << 16) |
                                (static_cast<uint32_t>(frame[5]) << 8) |
                                static_cast<uint32_t>(frame[6]);
    lastRemoteFrame = formatRemoteFrameHex(frame);

    if (commandHigh != kRemoteHighFan)
    {
      lastRemoteCommand = F("other");
      return true;
    }

    if (commandLow == kRemoteLowFanOff)
    {
      lastRemoteCommand = F("fan_off");
      applyFanSpeed(FanSpeed::Off);
      return true;
    }

    if (commandLow == kRemoteLowFanLow)
    {
      lastRemoteCommand = F("fan_1");
      applyFanSpeed(FanSpeed::Low);
      return true;
    }

    if (commandLow == kRemoteLowFanMid)
    {
      lastRemoteCommand = F("fan_2");
      applyFanSpeed(FanSpeed::Mid);
      return true;
    }

    if (commandLow == kRemoteLowFanHigh)
    {
      lastRemoteCommand = F("fan_3");
      applyFanSpeed(FanSpeed::High);
      return true;
    }

    if (commandLow == kRemoteLowLightToggle)
    {
      lastRemoteCommand = F("light_toggle");
      setLightEffectEnabled(!lightEffectEnabled);
      return true;
    }

    lastRemoteCommand = F("fan_unknown");
    return true;
  }

  void processRemotePayload(const String &payload)
  {
    if (payload.length() < 8)
    {
      return;
    }

    // Generated by Copilot: Scan payload for complete 8-byte IFAN remote frames.
    for (size_t index = 0; index + 7 < payload.length(); ++index)
    {
      uint8_t frame[8];
      for (size_t offset = 0; offset < 8; ++offset)
      {
        frame[offset] = static_cast<uint8_t>(payload[index + offset]);
      }

      if (parseRemoteFrame(frame))
      {
        index += 7;
      }
    }
  }

