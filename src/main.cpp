void setup()
{
  initLEDs();
  setLightEffectEnabled(false);

  pinMode(FAN_RELAY_1_PIN, OUTPUT);
  pinMode(FAN_RELAY_2_PIN, OUTPUT);
  pinMode(FAN_RELAY_3_PIN, OUTPUT);
  applyFanSpeed(FanSpeed::Off);

  Serial.begin(REMOTE_SIGNAL_BAUD);

  if (!LittleFS.begin())
  {
    LittleFS.format();
    LittleFS.begin();
  }

  loadPersistedConfig();

  if (!connectToWifi())
  {
    startFallbackAp();
  }

  setupOta();
  setupWebServer();
}

void loop()
{
  while (Serial.available())
  {
    const char current = static_cast<char>(Serial.read());
    appendRawUartByte(static_cast<uint8_t>(current));
    signalBuffer += current;
    if (signalBuffer.length() >= kMaxSignalBufferBytes || current == '\n' || current == '\r')
    {
      flushSignalBuffer();
    }
    lastSignalByteAtMs = millis();
  }

  if (signalBuffer.length() > 0 && millis() - lastSignalByteAtMs > kSignalFrameGapMs)
  {
    flushSignalBuffer();
  }

  setupOta();
  if (otaReady)
  {
    ArduinoOTA.handle();
  }

  webServer.handleClient();
  updateLightEffect();

  if (scheduledRestartAtMs != 0 && millis() >= scheduledRestartAtMs)
  {
    ESP.restart();
  }
}
